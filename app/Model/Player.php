	public function isOwn($id){
		$options = array('conditions' => array('Player.' . $this->Player->primaryKey => $id));
		$player = $this->Player->find('first', $options);
		if($player['Player']['user_id'] == $this->Auth->user('id'))
		{
			return TRUE;
		}
		else{
			return FALSE;
		}
	}
	
	public function sellInProgress($id_player){
		$options = array('conditions' => array('Transfer.player_id' => $id_player, 'activ' => 1));
		$player = $this->Transfer->find('first', $options);
		if($player)
		{
			return $player['Transfer']['id'];
		}
		else{
			return FALSE;
		}
	}
